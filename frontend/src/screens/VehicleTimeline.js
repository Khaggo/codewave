function TimelineEvent({ event, isLast }) {
  const cfg    = EVENT_CONFIG[event.type] ?? EVENT_CONFIG.admin
  const Icon   = cfg.icon
  const isAdmin    = event.category === 'administrative'
  const isVerified = event.isVerified
  const dateStr    = new Date(event.date).toLocaleDateString('en-PH', {
    weekday: 'short', year: 'numeric', month: 'short', day: 'numeric',
  })

  return (
    <div className="relative flex gap-4">
      {/* Vertical connector */}
      {!isLast && (
        <div className="absolute left-4 top-10 bottom-0 w-px" style={{ backgroundColor: '#27272a' }} />
      )}

      {/* Icon node */}
      <div className="flex-shrink-0 w-8 h-8 rounded-full flex items-center justify-center border-2 z-10"
           style={{ backgroundColor: cfg.bg, borderColor: cfg.color }}>
        <Icon size={13} style={{ color: cfg.color }} />
      </div>

      {/* Content card */}
      <div className="flex-1 mb-5">
        <div className="card p-4 hover:bg-surface-hover transition-colors">
          <div className="flex flex-wrap items-start justify-between gap-2 mb-2">
            <div className="flex flex-wrap items-center gap-2">
              <span className="text-xs font-bold uppercase tracking-wider px-2 py-0.5 rounded"
                    style={{ color: cfg.color, backgroundColor: cfg.bg }}>
                {cfg.label}
              </span>
              {isAdmin && (
                <span className="badge badge-gray">Administrative Event</span>
              )}
              {isVerified && (
                <span className="badge badge-green flex items-center gap-1">
                  <CheckCircle2 size={10} /> Verified by Staff
                </span>
              )}
            </div>
            <p className="text-xs text-ink-muted flex-shrink-0">{dateStr}</p>
          </div>

          <p className="text-sm text-ink-primary">{event.description}</p>

          <div className="flex flex-wrap items-center gap-3 mt-2.5">
            {event.technicianName && (
              <p className="text-xs text-ink-muted">
                <span className="text-ink-secondary font-medium">Technician:</span> {event.technicianName}
              </p>
            )}
            {event.jobOrderId && (
              <p className="text-xs text-ink-muted">
                <span className="text-ink-secondary font-medium">JO:</span>{' '}
                <span className="font-mono" style={{ color: '#f07c00' }}>{event.jobOrderId}</span>
              </p>
            )}
          </div>

          {/* Inspection record link — only for verified events */}
          {isVerified && event.jobOrderId && (
            <button className="mt-2.5 flex items-center gap-1.5 text-xs font-semibold hover:underline"
                    style={{ color: '#f07c00' }}>
              <ExternalLink size={11} /> View Inspection Record
            </button>
          )}
        </div>
      </div>
    </div>
  )
}

// ── Main ──────────────────────────────────────────────────────────────────────
export default function VehicleTimeline() {
  const [selectedId, setSelectedId] = useState('v1')
  const [filter,     setFilter]     = useState('all')   // 'all' | 'verified' | 'admin'

  const vehicle = allVehicles.find(v => v.id === selectedId)

  const events = [...timelineEvents]
    .filter(e => e.vehicleId === selectedId)
    .filter(e => {
      if (filter === 'verified') return e.isVerified
      if (filter === 'admin')    return e.category === 'administrative'
      return true
    })
    .sort((a, b) => new Date(b.date) - new Date(a.date))

  return (
    <div className="space-y-5 max-w-3xl">

      {/* ── Controls ──────────────────────────────── */}
      <div className="flex flex-wrap gap-3">
        {/* Vehicle selector */}
        <select value={selectedId} onChange={e => setSelectedId(e.target.value)}
          className="select flex-1 min-w-[200px] max-w-xs">
          {allVehicles.map(v => (
            <option key={v.id} value={v.id}>{v.plate} — {v.owner} ({v.model})</option>
          ))}
        </select>

        {/* Filter chips */}
        <div className="flex gap-2">
          {[
            { key: 'all',      label: 'All Events' },
            { key: 'verified', label: 'Verified Milestones' },
            { key: 'admin',    label: 'Administrative' },
          ].map(f => (
            <button key={f.key} onClick={() => setFilter(f.key)}
              className={`px-3 py-1.5 rounded-lg text-xs font-semibold transition-all border ${
                filter === f.key
                  ? 'text-white border-transparent'
                  : 'border-surface-border text-ink-secondary hover:bg-surface-hover'
              }`}
              style={filter === f.key ? { backgroundColor: '#f07c00', borderColor: '#f07c00' } : {}}>
              {f.label}
            </button>
          ))}
        </div>
      </div>

      {/* ── Vehicle summary chip ──────────────────── */}
      {vehicle && (
        <div className="flex flex-wrap items-center gap-3 p-4 card">
          <div>
            <p className="text-sm font-bold text-ink-primary">{vehicle.owner}</p>
            <p className="text-xs text-ink-secondary">{vehicle.year} {vehicle.model} · {vehicle.color}</p>
          </div>
          <div className="ml-auto flex flex-wrap gap-2">
            <span className="font-mono text-xs px-2 py-1 rounded font-bold"
                  style={{ backgroundColor: 'rgba(240,124,0,0.1)', color: '#f07c00' }}>
              {vehicle.plate}
            </span>
            <span className={`badge ${vehicle.status === 'active' ? 'badge-green' : vehicle.status === 'maintenance' ? 'badge-orange' : 'badge-gray'}`}>
              {vehicle.status}
            </span>
          </div>
        </div>
      )}

      {/* ── AI Summary card ───────────────────────── */}
      <AISummaryCard vehicleId={selectedId} />

      {/* ── Timeline ──────────────────────────────── */}
      <div className="pt-2">
        {events.length === 0
          ? (
            <div className="text-center py-16 text-ink-muted text-sm">
              No events found for this filter.
            </div>
          )
          : events.map((ev, i) => (
              <TimelineEvent key={ev.id} event={ev} isLast={i === events.length - 1} />
            ))
        }
      </div>
    </div>
  )
}
