'use client'

import { useState } from 'react'
import { Wrench, CheckCircle2, AlertCircle, Clock, ShoppingBag,
         Shield, Sparkles, ExternalLink, BadgeCheck } from 'lucide-react'
import { vehicles as allVehicles, timelineEvents } from '@/lib/mockData'

